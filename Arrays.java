public class Arrays {
    public static void main(String[] args) {
        int[] arr = { 1, 2, 3, 4, 5 }; // Example array
        int[] arr2;

        arr2 = new int[5]; // Create a new array of size 5

        int arr3[] = new int[5]; // Another way to declare an array

        System.out.println("arr: " + arr); // Prints the reference to the array object
        System.out.println("arr2: " + arr2); // Prints the reference to the array object
        System.out.println("arr3: " + arr3); // Prints the reference to the array object
    }
}
